package text;

public class Exam02 {
 
	public static void main(String[] args) {
	
		int num1=5;
		int num2=2;
		
		System.out.println(num1/num2); 
		System.out.println(5/2); 
		//정수/정수 = 정수 그리고 값이 2로 뜬다, 실수/실수 = 실수
		
		System.out.println(num1/(float)num2);
		// (float)를 입력하여 실수로 계산해 달라고 명령.
	}
	
}
